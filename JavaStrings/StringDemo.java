package JavaStrings;

public class StringDemo {
    public static void main(String[] args) {
        // String is sequence of characters (array of character)
        char myVar = 65;
        System.out.println(myVar);
        char[] ch = {'S','u','m','i','t'};
        String charArray = new String(ch);
        System.out.println(charArray);
    }
}
